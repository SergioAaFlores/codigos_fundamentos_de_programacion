ficha={
    'nombre': input('Ingresa tu nombre: '),
    'telefono': int(input('Ingresa tu numero: ')),
    'email': input('Ingresa tu email: ').lower().replace(" ",""),
    'edad': int(input('Ingresa tu edad: '))

}
while True :
  print('=== MENU ===')
  try:
    ocp=int(input('[1]Ver ficha[2]Editar dato[3]Salir'))
  except ValueError:
    print('Error: tiene que ser un numero')
    continue
  if ocp == 1:
    print('==== FICHA ====')
    print('Nombre  :',ficha['nombre'])
    print('Telefono:',ficha['telefono'])
    print('Email   :',ficha['email'])
    print('Edad    :',ficha['edad'])
  elif ocp == 2:
    try:
      editar=input('Ingresa el campo que desea editar: ').lower().replace(" ","")
    except:
      print('Error!!!')
      break
    if editar == 'nombre':
      cambio=input('Ingresa el nombre nuevo: ')
      ficha['nombre']=cambio
    elif editar == 'telefono':
      cambio=int(input('Ingresa el nombre nuevo: '))
      ficha['telefono']=cambio
    elif editar == 'email':
      cambio=input('Ingresa el nombre nuevo: ')
      ficha['email']=cambio
    elif editar == 'edad':
      cambio=int(input('Ingresa el nombre nuevo: '))
      ficha['edad']=cambio
  elif ocp == 3:
    print('==== Saliendo del sistema ====')
    break
  else:
    print('Opcion invalida')